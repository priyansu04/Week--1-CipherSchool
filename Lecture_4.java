public class Lecture_4 {
    public static void main(String[] args) {
        // Initializing variables x and y with values 5 and 7
        int x = 5;
        int y = 7;

        // Printing the values of variables x and y
        System.out.println("X is: " + x);
        System.out.println("Y is: " + y);

        // Declaring and initializing byte variable b with value 10
        byte b = 10;

        // Implicitly converting byte b to int and storing it in variable i
        int i = b;

        // Printing the value of variable i
        System.out.println("I is: " + i);
    }
}
